package uk.ac.cam.cl.ticking.ui.actors;

/**
 * @author tl364
 *
 *         Submitter - Has the ability to complete ticks associated with the
 *         group.
 *
 *         Marker - Has the ability to mark ticks associated with the group.
 *
 *         Author - Has the ability to write new ticks for the group.
 *
 *         Overview - Has the ability to see overall statistics regarding who
 *         has completed what.
 *
 */
public enum Role {
	SUBMITTER, MARKER, AUTHOR, OVERVIEW;

}
